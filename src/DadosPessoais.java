
public class DadosPessoais {

	// Atributos
	String nomeAt;
	int idadeAt;
	int registroAT;

	// Construtores superclass = classe pai
	public DadosPessoais() {
		super();
	}

	// Getters
	public String getNomeAt() {
		return nomeAt;
	}
	public void setNomeAt(String nomeAt) {
		this.nomeAt = nomeAt;
	}
	public int getIdadeAt() {
		return idadeAt;
	}
	public void setIdadeAt(int idadeAt) {
		this.idadeAt = idadeAt;
	}
	public int getRegistroAT() {
		return registroAT;
	}
	public void setRegistroAT(int registroAT) {
		this.registroAT = registroAT;
	}
	
	
	
}
